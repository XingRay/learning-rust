/// # Lesson 001 - Hello, World!
///
/// 这是 Rust 学习之旅的第一课。
///
/// ## 学习目标
/// - 理解 Rust 程序的基本结构
/// - 了解 `fn main()` 入口函数
/// - 学会使用 `println!` 宏进行输出
/// - 了解 Rust 的编译与运行流程
///
/// ## 运行方式
/// 在项目根目录下执行:
/// ```bash
/// cargo run -p lesson001_helloworld
/// ```

// =============================================================
// Lesson 001: Hello, World! - 你的第一个 Rust 程序
// =============================================================

/// 每个 Rust 程序都从 `main` 函数开始执行。
/// `fn` 是 function（函数）的缩写，用于定义函数。
/// `main` 是一个特殊的函数名，它是程序的入口点。
fn main() {
    // ---------------------------------------------------------
    // 1. 基本输出
    // ---------------------------------------------------------
    // `println!` 是一个宏（macro），注意末尾的感叹号 `!`
    // 宏和函数类似，但它在编译时会展开为更多的代码
    println!("Hello, World!");
    println!("你好，Rust！欢迎来到 Rust 的世界！");

    // ---------------------------------------------------------
    // 2. 格式化输出
    // ---------------------------------------------------------
    // 使用 `{}` 作为占位符，类似其他语言的格式化字符串
    let language = "Rust";
    let year = 2015; // Rust 1.0 发布于 2015 年
    println!("{} 语言于 {} 年发布 1.0 版本", language, year);

    // 也可以使用位置参数
    println!("{0} 是一门系统编程语言，{0} 注重安全性和性能", language);

    // 使用命名参数
    println!(
        "{name} 的吉祥物是一只螃蟹，名叫 {mascot}",
        name = "Rust",
        mascot = "Ferris"
    );

    // ---------------------------------------------------------
    // 3. 转义字符与特殊输出
    // ---------------------------------------------------------
    // 换行符 \n，制表符 \t
    println!("第一行\n第二行");
    println!("名字\t年龄\t城市");
    println!("Alice\t30\t北京");

    // 输出花括号本身，使用 {{ 和 }}
    println!("在 Rust 中，{{}} 是格式化占位符");

    // ---------------------------------------------------------
    // 4. 其他打印宏
    // ---------------------------------------------------------
    // `print!` - 不换行的打印
    print!("这不会换行...");
    println!("看，我们在同一行！");

    // `eprintln!` - 打印到标准错误输出（stderr）
    eprintln!("这是一条错误信息（输出到 stderr）");

    // `dbg!` - 调试宏，会打印表达式的值和位置信息
    let x = 5 * 2;
    dbg!(x); // 输出: [src/main.rs:行号] x = 10

    // ---------------------------------------------------------
    // 5. 小练习（取消注释试试看）
    // ---------------------------------------------------------
    // 练习 1: 打印你的名字
    // println!("我的名字是 {}", "你的名字");

    // 练习 2: 打印一个简单的图案
    // println!("  *  ");
    // println!(" *** ");
    // println!("*****");
    // println!(" *** ");
    // println!("  *  ");

    // 练习 3: 计算并打印结果
    // let a = 10;
    // let b = 20;
    // println!("{} + {} = {}", a, b, a + b);

    println!("\n🎉 恭喜！你已经完成了第一课！");
}
