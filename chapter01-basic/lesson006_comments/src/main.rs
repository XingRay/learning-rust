/// 将两个整数相加并返回结果。
///
/// # 参数
///
/// * `a` - 第一个加数
/// * `b` - 第二个加数
///
/// # 返回值
///
/// 返回 `a + b` 的结果。
///
/// # 示例
///
/// ```
/// let result = add(2, 3);
/// assert_eq!(result, 5);
/// ```
fn add(a: i32, b: i32) -> i32 {
    a + b
}

/// 计算非负整数的阶乘。
///
/// 使用递归算法计算 `n!`。
///
/// # 参数
///
/// * `n` - 要计算阶乘的非负整数
///
/// # 返回值
///
/// 返回 `n!` 的值。`0! = 1`。
///
/// # 示例
///
/// ```
/// assert_eq!(factorial(0), 1);
/// assert_eq!(factorial(5), 120);
/// ```
///
/// # 注意
///
/// 当 `n` 较大时可能发生溢出。对于 `u64` 类型，
/// `n <= 20` 时结果不会溢出。
fn factorial(n: u64) -> u64 {
    if n <= 1 {
        1
    } else {
        n * factorial(n - 1)
    }
}

/// 将摄氏温度转换为华氏温度。
///
/// 转换公式: `F = C × 9/5 + 32`
///
/// # 参数
///
/// * `celsius` - 摄氏温度
///
/// # 返回值
///
/// 返回一个元组 `(摄氏温度, 华氏温度)`。
///
/// # 示例
///
/// ```
/// let (c, f) = celsius_to_fahrenheit(0.0);
/// assert_eq!(c, 0.0);
/// assert_eq!(f, 32.0);
///
/// let (c, f) = celsius_to_fahrenheit(100.0);
/// assert_eq!(c, 100.0);
/// assert_eq!(f, 212.0);
/// ```
fn celsius_to_fahrenheit(celsius: f64) -> (f64, f64) {
    let fahrenheit = celsius * 9.0 / 5.0 + 32.0;
    (celsius, fahrenheit)
}

/// 创建一个问候语字符串。
///
/// # 参数
///
/// * `name` - 要问候的名字
///
/// # 示例
///
/// ```
/// let greeting = make_greeting("World");
/// assert_eq!(greeting, "Hello, World!");
/// ```
///
/// 也可以用中文名:
///
/// ```
/// let greeting = make_greeting("世界");
/// assert_eq!(greeting, "Hello, 世界!");
/// ```
fn make_greeting(name: &str) -> String {
    format!("Hello, {}!", name)
}

/// 一个二维平面上的点。
///
/// `Point` 结构体表示二维坐标系中的一个点，
/// 支持基本的几何运算。
///
/// # 示例
///
/// ```
/// let origin = Point::new(0.0, 0.0);
/// let p = Point::new(3.0, 4.0);
/// assert_eq!(p.distance_to_origin(), 5.0);
/// ```
#[derive(Debug)]
struct Point {
    /// x 坐标
    x: f64,
    /// y 坐标
    y: f64,
}

impl Point {
    /// 创建一个新的 `Point`。
    ///
    /// # 参数
    ///
    /// * `x` - x 坐标值
    /// * `y` - y 坐标值
    ///
    /// # 示例
    ///
    /// ```
    /// let p = Point::new(1.0, 2.0);
    /// ```
    fn new(x: f64, y: f64) -> Self {
        Point { x, y }
    }

    /// 计算该点到原点 `(0, 0)` 的距离。
    ///
    /// 使用勾股定理: `sqrt(x² + y²)`
    ///
    /// # 示例
    ///
    /// ```
    /// let p = Point::new(3.0, 4.0);
    /// assert!((p.distance_to_origin() - 5.0).abs() < 1e-10);
    /// ```
    fn distance_to_origin(&self) -> f64 {
        (self.x * self.x + self.y * self.y).sqrt()
    }
}

