// =============================================================

fn main() {
    println!("=== Lesson 006: 注释与文档 ===\n");

    // ---------------------------------------------------------
    // 1. 行注释 //
    // ---------------------------------------------------------
    println!("--- 1. 行注释 // ---");

    // 这是一个行注释
    // 行注释从 // 开始，一直到行尾
    let x = 5; // 行尾注释也是可以的
    println!("x = {} （行尾有注释）", x);

    // 行注释可以用来临时禁用代码：
    // let unused = 42;
    // println!("这行不会执行");

    // 多行注释就是连续的行注释
    // 第一行
    // 第二行
    // 第三行

    println!("行注释是最常用的注释方式");

    // ---------------------------------------------------------
    // 2. 块注释 /* */
    // ---------------------------------------------------------
    println!("\n--- 2. 块注释 /* */ ---");

    /* 这是一个块注释 */

    /* 块注释可以
       跨越
       多行 */

    let y = /* 块注释可以出现在代码中间 */ 10;
    println!("y = {} （代码中间有块注释）", y);

    /* Rust 的块注释可以嵌套！这和 C/C++ 不同 */
    /* 外层注释
        /* 内层嵌套注释 */
       外层注释继续
    */
    println!("Rust 块注释支持嵌套，这比 C/C++ 更方便");

    // 块注释临时禁用代码块
    /*
    let a = 1;
    let b = 2;
    println!("这整段都被注释掉了: {}", a + b);
    */

    // ---------------------------------------------------------
    // 3. 文档注释 ///
    // ---------------------------------------------------------
    println!("\n--- 3. 文档注释 /// ---");

    // /// 是外部文档注释，用于描述紧跟在它后面的项（函数、结构体等）
    // 它支持 Markdown 格式！

    // 调用有文档注释的函数
    let result = add(3, 5);
    println!("add(3, 5) = {}", result);

    let result = factorial(5);
    println!("factorial(5) = {}", result);

    let (c, f) = celsius_to_fahrenheit(100.0);
    println!("{:.1}°C = {:.1}°F", c, f);

    // 调用有文档的结构体
    let point = Point::new(3.0, 4.0);
    println!("点 {:?} 到原点的距离: {:.2}", point, point.distance_to_origin());

    println!("\n提示: 运行 `cargo doc -p lesson006_comments --open` 查看生成的文档");

    // ---------------------------------------------------------
    // 4. 内部文档注释 //!
    // ---------------------------------------------------------
    println!("\n--- 4. 内部文档注释 //! ---");

    // //! 注释用于描述「包含它的项」，通常用于：
    // 1. 在 lib.rs 或 main.rs 顶部描述整个 crate
    // 2. 在模块内部描述该模块

    println!("//! 用于 crate 或模块级文档");
    println!("通常放在文件顶部，描述该文件/模块的用途");
    println!("本文件顶部就有一段 //! 注释（查看源码）");

    // //! 和 /// 的区别:
    // /// 描述「紧跟在它后面的项」（外部文档）
    // //! 描述「包含它的项」（内部文档）
    //
    // 示例:
    //   //! 这是模块文档（放在模块内部开头）
    //   /// 这是函数文档（放在函数定义前面）
    //   fn my_function() {}

    println!("\n/// 和 //! 的区别:");
    println!("  /// → 描述紧跟在它后面的项（外部文档注释）");
    println!("  //! → 描述包含它的项/模块（内部文档注释）");

    // ---------------------------------------------------------
    // 5. 文档中的代码示例
    // ---------------------------------------------------------
    println!("\n--- 5. 文档中的代码示例 ---");

    // 在文档注释中用 ```rust ``` 写代码块
    // 这些代码会被 cargo test 自动作为测试运行！

    let greeting = make_greeting("Rust");
    println!("{}", greeting);

    println!("\n文档代码示例的特殊标记:");
    println!("  ```rust          — 普通 Rust 代码（可运行测试）");
    println!("  ```no_run        — 可编译但不运行");
    println!("  ```ignore        — 完全忽略");
    println!("  ```should_panic  — 预期 panic");
    println!("  ```compile_fail  — 预期编译失败");
    println!("  # 隐藏行         — 以 # 开头的行在文档中隐藏，但参与编译");

    // ---------------------------------------------------------
    // 6. #[doc] 属性
    // ---------------------------------------------------------
    println!("\n--- 6. #[doc] 属性 ---");

    // /// 实际上是 #[doc = "..."] 的语法糖
    // 以下两种写法等价：
    //
    // /// 这是文档
    // fn foo() {}
    //
    // #[doc = "这是文档"]
    // fn foo() {}

    let hidden_fn_result = hidden_doc_example();
    println!("带 #[doc] 属性的函数返回: {}", hidden_fn_result);

    println!("\n常用的 doc 属性:");
    println!("  #[doc = \"...\"]        — 等价于 ///");
    println!("  #[doc(hidden)]        — 在文档中隐藏此项");
    println!("  #[doc(alias = \"...\"] — 为文档搜索添加别名");

    // ---------------------------------------------------------
    // 7. 注释最佳实践
    // ---------------------------------------------------------
    println!("\n--- 7. 注释最佳实践 ---");

    println!("好的注释原则:");
    println!("  1. 解释「为什么」而不是「是什么」");
    println!("  2. 公共 API 必须写文档注释");
    println!("  3. 使用 Markdown 格式使文档更美观");
    println!("  4. 文档代码示例保持可运行");
    println!("  5. 避免过期注释（及时更新）");

    // 好的注释示例：
    // ✅ 解释为什么使用特定算法
    // 使用二分查找而不是线性搜索，因为数据已排序且规模较大
    let _sorted_data = vec![1, 3, 5, 7, 9, 11, 13, 15];

    // ❌ 不好的注释（只描述代码做什么，显而易见）
    // let x = x + 1; // x 加 1

    println!("\nTODO/FIXME/HACK 注释:");
    // TODO: 后续需要实现的功能
    // FIXME: 已知的问题需要修复
    // HACK: 临时的解决方案，后续需要改进
    // NOTE: 重要说明
    // SAFETY: unsafe 代码的安全性说明
    println!("  // TODO:  待实现的功能");
    println!("  // FIXME: 需要修复的问题");
    println!("  // HACK:  临时方案");
    println!("  // NOTE:  重要说明");
    println!("  // SAFETY: unsafe 安全性说明");

    // ---------------------------------------------------------
    // 8. 小结
    // ---------------------------------------------------------
    println!("\n--- 小结 ---");
    println!("✅ // 行注释，最常用");
    println!("✅ /* */ 块注释，支持嵌套");
    println!("✅ /// 外部文档注释，用 Markdown 描述紧跟的项");
    println!("✅ //! 内部文档注释，描述包含它的模块/crate");
    println!("✅ 文档中的代码示例可被 cargo test 测试");
    println!("✅ #[doc] 属性可以控制文档生成");
    println!("✅ cargo doc --open 生成并查看文档");

    println!("\n🎉 恭喜！你已经完成了第六课！");
}

