//! # Lesson 006 - 注释与文档
//!
//! 本课学习 Rust 中的注释和文档系统。
//!
//! ## 学习目标
//! - 掌握行注释（//）和块注释（/* */）
//! - 掌握文档注释（/// 和 //!）
//! - 了解 rustdoc 和文档属性
//! - 学会在文档中编写代码示例
//!
//! ## 运行方式
//! ```bash
//! cargo run -p lesson006_comments
//! ```
//!
//! ## 生成文档
//! ```bash
//! cargo doc -p lesson006_comments --open
//! ```
//!
//! ## 注释类型一览
//!
//! | 语法 | 用途 |
//! |------|------|
//! | `//` | 行注释 |
//! | `/* */` | 块注释 |
//! | `///` | 外部文档注释（紧跟在项之前） |
//! | `//!` | 内部文档注释（描述包含它的项） |
//!
//! 注意: `//!` 内部文档注释必须放在文件最顶部（所有 item 之前），
//! 用于描述当前 crate 或模块。本文件的顶部就是用 `//!` 编写的。

